// 오늘의 기사 가져오기 (데모: 날짜 기반 인덱스)
export function getTodayArticle() {
    const today = new Date();
    const index = today.getDate() % sampleArticles.length;
    return sampleArticles[index];
}

// 랜덤 기사 가져오기
export function getRandomArticle() {
    const index = Math.floor(Math.random() * sampleArticles.length);
    return sampleArticles[index];
}

// 특정 ID로 기사 가져오기
export function getArticleById(id) {
    return sampleArticles.find(article => article.id === id);
}
